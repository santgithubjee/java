class Main {
  public static void main(String[] args) {
    //java data type (primitive data
    // 8 primitive data type 
    
    // 1. boolean type
    boolean flag = true;
    System.out.println(flag);

    //2. byte type
    byte range ;
    range = 124;
    System.out.println(range);

    //3. short type
    short tem;
    tem=3534;
    System.out.println(tem);

    //4. int type
    int a;
    a=2343;
    System.out.println(a);

  //5. long type
    long b;
    b=-344364546l;
    System.out.println(b);

    //6. double type 
    double num;
    num = -34.566;
    System.out.println(num);

    //7. floating point 
    float num1;
    num1 = 4.6F;
    System.out.println(num1);

    //8. char type 
    char letter = '\u0051';
    System.out.println(letter);

    // string type 
    String mystr;
    mystr = "my java code ";
    
    
    System.out.println("everything is fine !");
  }
}